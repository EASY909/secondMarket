<template>
  <div id="login">
    <!-- <el-button type="text" @click="regist = true">打开嵌套表单的 Dialog</el-button> -->

    <el-dialog
      title="欢迎加入江大二手市场"
      @close="close"
      :modal-append-to-body="false"
      :visible.sync="dialogFormVisible"
      center
      width="30%"
    >
      <el-form :model="form">
        <el-form-item label="手机" :label-width="formLabelWidth">
          <el-input v-model="form.phone" autocomplete="off"></el-input>
        </el-form-item>
        <el-form-item label="密码" :label-width="formLabelWidth">
          <el-input show-password v-model="form.password" autocomplete="off"></el-input>
        </el-form-item>
      </el-form>

      <span slot="footer" class="dialog-footer">
        <el-button type="primary" @click="doLogin">登录</el-button>
        <p>
          没有账号？赶快
          <span class="login" @click="goRegist">注册</span>吧！
        </p>
      </span>
    </el-dialog>
  </div>
</template>

<script>
//这里可以导入其他文件（比如：组件，工具js，第三方插件js，json文件，图片文件等等）
//例如：import 《组件名称》 from '《组件路径》';

export default {
  //import引入的组件需要注入到对象中才能使用
  name: "logintindex",

  props: {
    loginFlag: {
      type: Boolean,
      default: false
    }
  },
  data() {
    //这里存放数据
    return {
      dialogFormVisible: false,
      form: {
        password: "",
        phone: ""
      },
      formLabelWidth: "100px"
    };
  },
  //监听属性 类似于data概念
  //   computed: {
  //     dialogFormVisible(nv, ov) {
  //       return;
  //     }
  //   },
  //监控data中的数据变化
  watch: {
    // registFlag(nv,ov) {
    //     console.log(1)
    //     this.dialogFormVisible = nv;
    // }
    loginFlag(nv, ov) {
      this.dialogFormVisible = nv;
    }
  },
  //方法集合
  methods: {
    close() {
      this.dialogFormVisible = false;
      this.$emit("update:loginFlag", false);
    },
    goRegist() {
      this.dialogFormVisible = false;
      this.$emit("update:loginFlag", false);
      this.$emit("goRegist", true);
    },
    doLogin() {
      let reData = {
        phone: this.form.phone,
        password: this.form.password
      };
      this.$store
        .dispatch("login/login", reData)
        .then(resonse => {
          if (resonse.code == 1) {
            this.$message({
              message: "登录成功！",
              type: "success"
            });
            this.close();
          }
          
        })
        .catch(error => {});
    }
  },
  //生命周期 - 创建完成（可以访问当前this实例）
  created() {
    // console.log(this.$props.registFlag);
  },
  //生命周期 - 挂载完成（可以访问DOM元素）
  mounted() {},
  beforeCreate() {}, //生命周期 - 创建之前
  beforeMount() {}, //生命周期 - 挂载之前
  beforeUpdate() {}, //生命周期 - 更新之前
  updated() {}, //生命周期 - 更新之后
  beforeDestroy() {}, //生命周期 - 销毁之前
  destroyed() {}, //生命周期 - 销毁完成
  activated() {} //如果页面有keep-alive缓存功能，这个函数会触发
};
</script>
